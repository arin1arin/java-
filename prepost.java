public class prepost {
  public static void main(String[] args) {
      int a=10;
      System.out.println(a);
      System.out.println("PrePost incre...");
      System.out.println(a++);
      System.out.println(++a);

      System.out.println("PrePost Decr..");
      System.out.println(a--);
      System.out.println(--a);
  }
}
